package cs;

public class ArithmeticOperator {

	public static void main(String[] args) {
		int x = 10, y = 7;
		double z = 2.5;
		System.out.println("x + y = " + (x+y));
		System.out.println("x - y = " + (x-y));
		System.out.println("x * y = " + (x*y));
		System.out.println("x / z = " + (x/z));
		System.out.println("x % y = " + (x%y));
		System.out.println("x / y = " + (x/y));
	}

}
