package cs;

public class ComparisonOperator {

	public static void main(String[] args) {
		int x = 10, y = 7;
		double z = 2.5;
		System.out.println("x > y = " + (x>y));
		System.out.println("x < y = " + (x<y));
		System.out.println("x >= z = " + (x>=z));
		System.out.println("y <= z = " + (y<=z));
		System.out.println("y != x = " + (y!=x));
		System.out.println("y == 7 = " + (y==7));
	}

}
